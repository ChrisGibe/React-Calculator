/**
 * Utils class hold the methods to execute all the Math operations
 *
 */
export default class Utils {
  static addition(a, b) {
    return a + b;
  }
  static substraction(a, b) {
    return a - b;
  }
  static multiplication(a, b) {
    return a * b;
  }
  static division(a, b) {
    return a / b;
  }
}
